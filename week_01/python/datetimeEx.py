import datetime  # datetime 모듈을 불러옵니다. 이 모듈은 날짜와 시간 관련 작업을 처리하는데 사용됩니다.


def main():
    # 현재 날짜와 시간을 가져옵니다.
    now = (
        datetime.datetime.now()
    )  # now()는 현재 날짜와 시간의 datetime 객체를 반환합니다.

    # 현재 시간이 오전인지 오후인지 확인합니다.
    if now.hour < 12:  # 현재 시간의 'hour'가 12보다 작으면 오전입니다.
        print(f"현재 시각은 {now.hour}시로 오전입니다.")  # 오전을 출력합니다.
    if now.hour >= 12:  # 현재 시간의 'hour'가 12 이상이면 오후입니다.
        print(f"현재 시각은 {now.hour}시로 오후입니다.")  # 오후를 출력합니다.

    # 계절을 판단하기 위한 조건문: 현재 월을 기준으로 계절을 출력합니다.
    # 팁: 'month' 값은 1월(1)부터 12월(12)까지의 숫자입니다. 이를 기준으로 계절을 분류할 수 있습니다.

    if 2 < now.month < 6:  # 3, 4, 5월은 봄입니다. (2 < now.month < 6는 3, 4, 5를 포함)
        print(f"이번 달은 {now.month}월로 봄입니다.")  # 봄 출력
    elif 5 < now.month < 9:  # 6, 7, 8월은 여름입니다.
        print(f"이번 달은 {now.month}월로 여름입니다.")  # 여름 출력
    elif 8 < now.month < 12:  # 9, 10, 11월은 가을입니다.
        print(f"이번 달은 {now.month}월로 가을입니다.")  # 가을 출력
    else:  # 나머지 12, 1, 2월은 겨울입니다.
        print(f"이번 달은 {now.month}월로 겨울입니다.")  # 겨울 출력


# __name__은 현재 스크립트의 이름을 나타내며, "__main__"은 현재 스크립트가 직접 실행될 때만 조건을 만족합니다.
# 팁: 이 조건문을 사용하면, 이 파일을 다른 프로그램에서 모듈로 가져올 때 코드를 실행하지 않고 함수만 사용할 수 있습니다.
if (
    __name__ == "__main__"
):  # 현재 파일이 직접 실행될 때만 아래 코드를 실행하도록 합니다.
    main()  # main 함수 실행

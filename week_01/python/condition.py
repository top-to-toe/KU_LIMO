def main():
    # 조건문
    # 사용자로부터 숫자를 입력받습니다. 입력은 문자열이므로 int()를 사용하여 정수로 변환합니다.
    number = int(input("숫자를 넣어주세요: "))
    
    # number가 짝수인지 홀수인지를 확인합니다.
    if number % 2 == 0:  # number를 2로 나누었을 때 나머지가 0이면 짝수
        print("짝수입니다.")  # 짝수일 경우 이 메시지를 출력합니다.
    else:  # number가 짝수가 아니면 (즉, 홀수일 경우)
        print("홀수입니다.")  # 홀수일 경우 이 메시지를 출력합니다.

    # 반복문 for
    # range(10)은 0부터 9까지의 정수를 생성합니다. 
    # 따라서 for문은 0부터 9까지 반복됩니다.
    for i in range(10):  # 0부터 9까지 반복
        print(f" {i} 번째 반복되는 문장입니다.")  # 각 반복에서 i의 값을 출력합니다.
    
    # range(10)은 0부터 9까지의 숫자 리스트를 반환하므로, 이를 list()로 리스트 형태로 출력합니다.
    print(list(range(10)))  # range(10)의 결과를 리스트로 변환하여 출력합니다.

# __name__이 "__main__"일 때, 즉 이 파일이 직접 실행될 때 main() 함수를 호출합니다.
if __name__ == "__main__" :
    main()  # main() 함수를 실행

def main():  # main() 함수 정의 시작
    var_a = 100  # var_a라는 변수에 100을 할당합니다.
    
    print("variable a")  # 문자열 "variable a"를 출력합니다.
    print(100 + 100)  # 100과 100을 더한 값을 출력합니다 (200).
    print(100 * 100)  # 100과 100을 곱한 값을 출력합니다 (10000).
    print(100 - 50)  # 100에서 50을 뺀 값을 출력합니다 (50).
    
    print(var_a)  # var_a의 값을 출력합니다 (100).
    print("variable a : ", var_a)  # 문자열과 var_a의 값을 함께 출력합니다 ("variable a : 100").

if __name__ == "__main__":  # 이 파일이 직접 실행되었을 때만 아래 코드를 실행하도록 합니다.
    main()  # main() 함수를 호출하여 프로그램을 실행합니다.